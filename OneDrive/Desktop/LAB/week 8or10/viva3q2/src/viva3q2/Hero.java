
package viva3q2;



public class Hero {
    
    String name;
    String element;
    int power;
    
    public Hero(){
        name = "";
        element = "";
        power = 0;
    }
    
    public Hero(String name, String element, int power){
        this.name = name;
        this.element = element;
        this.power = power;
    }
    
}
